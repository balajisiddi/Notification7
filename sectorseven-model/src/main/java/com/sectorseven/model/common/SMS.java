package com.sectorseven.model.common;

public class SMS {

	    private String sToPhoneNo;
	    private String sMessage;

	    public String getsToPhoneNo() {
	        return sToPhoneNo;
	    }

	    public void setsToPhoneNo(String sToPhoneNo) {
	        this.sToPhoneNo = sToPhoneNo;
	    }

	    public String getsMessage() {
	        return sMessage;
	    }

	    public void setsMessage(String sMessage) {
	        this.sMessage = sMessage;
	    }
}
